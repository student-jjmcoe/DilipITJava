package com.shweta.selectionstatements;

public class IfStatementDemo {

	public static void main(String[] args) {
		System.out.println("Checking if given number is Even or Odd");
		int number = 368;
		
		if(number % 2 == 0) {
			System.out.println("In If block");
			System.out.println("Given number "+number+" is Even");
			System.out.println("leaving if block");
		}
		System.out.println("Checking is completed");
		System.out.println("Thank You");

	}

}
