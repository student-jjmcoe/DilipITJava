package com.shweta.operators;

public class ArithmeticOperators {

	public static void main(String[] args) {
		int value1 = 101;
		int value2 = 20;
		
		System.out.println(value1 + value2);//value1 and value2 are operands
		System.out.println(value1 - value2);
		System.out.println(value1 * value2);
		System.out.println(value1 / value2);
		System.out.println(value1 % value2);

	}

}
