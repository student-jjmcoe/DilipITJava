
public class EscapeSequences {
	public static void main(String[] args)
	{
		System.out.println("1Hello\tWorld");
		System.out.println("2Hello\bWorld");
		System.out.println("3Hello\nWorld");
		System.out.println("4Hello\rWorld");
		System.out.println("5Hello\fWorld");
		System.out.println("6Hello World \'Shweta\'");
		System.out.println("7Hello World \"Shweta\"");
		System.out.println("8Hello\\World");
	}

}
